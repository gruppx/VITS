    private void fillAssignment(){
        box_Assignment.removeAllItems();
        ResultSet assignments = db.getColumn("select * from assignment");
        int numOfAssignments;
        
        try
        {
            numOfAssignments = db.getCount("assignment"); 
            for(int i = 0; i < numOfAssignments; i++) {
                assignments.next();
                box_Assignment.addItem(assignments.getString("Name"));
            }
            
        }
        catch(Exception e)
        {
             JOptionPane.showMessageDialog(null, e.getMessage());
        }
        }
    
    private void fillBoxes(){
        box_FromCountry.removeAllItems();
        box_ToCountry.removeAllItems();
        ResultSet countries = db.getColumn("select * from allowance");
        int numOfCountries;
        try{
        numOfCountries = db.getCount("allowance");
        for (int i=0; i<numOfCountries; i++)
        {
            countries.next();
            box_FromCountry.addItem(countries.getString("Country"));
            box_ToCountry.addItem(countries.getString("Country"));
        }
            }
                catch(Exception e)
                {
                JOptionPane.showMessageDialog(null, e.getMessage());
                }
    }
    
     private void fillDay(){
        int [] date = new int[32];
        cbx_ADay.removeAllItems();
        cbx_DDay.removeAllItems();
       
        for(int i = 1; i < 32; i++)
        {
            if(i < 10){
            date[i] = i + 1;
            cbx_ADay.addItem("0" + i);
            cbx_DDay.addItem("0" + i);
        }
        else if(i >= 10)
        {
             date[i] = i + 1;
            cbx_ADay.addItem(i);
            cbx_DDay.addItem(i); 
        }
       }
    }
      

  private void fillMonth(){
       int [] month = new int[13];
       cbx_AMonth.removeAllItems();
       cbx_DMonth.removeAllItems();
       
       for(int i = 1; i < 13; i++){
           if(i < 10){
           cbx_AMonth.addItem("0" + i);
           cbx_DMonth.addItem("0" + i);
           month[i] = i + 1;
           }
           else if(i >= 10)
           {
           cbx_AMonth.addItem(i);
           cbx_DMonth.addItem(i); 
           month[i] = i + 1;
           }
       }
       
    }
        
  
   
    private void fillYear(){
       int [] year = new int[99];
       cbx_AYear.removeAllItems();
       cbx_DYear.removeAllItems();
       
       for(int i = 1; i < 99; i++){
           if(i < 10){
           cbx_AYear.addItem("200" + i);
           cbx_DYear.addItem("200" + i);
           year[i] = i + 1;
           }
           else if(i >= 10)
           {
           cbx_AYear.addItem("20" + i);
           cbx_DYear.addItem("20" + i); 
           year[i] = i + 1;
           }
       }
       
    }

    
    
    
    
    
    
    
