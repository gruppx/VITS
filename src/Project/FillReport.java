     private void fillDay(){
        int [] date = new int[32];
        cbx_ADay.removeAllItems();
        cbx_DDay.removeAllItems();
       
        for(int i = 1; i < 32; i++)
        {
            if(i < 10){
            date[i] = i + 1;
            cbx_ADay.addItem("0" + i);
            cbx_DDay.addItem("0" + i);
        }
        else if(i >= 10)
        {
             date[i] = i + 1;
            cbx_ADay.addItem(i);
            cbx_DDay.addItem(i); 
        }
       }
    }
      

